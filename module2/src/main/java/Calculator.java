public class Calculator {
    void add(final int m, final int n) {
        int add;
        add = m + n;

        System.out.println("The sum is: " +add);

    }

    void subtract(final int a, final int b) {
        int sub;
        sub = a - b;

        System.out.println("The sub is: " +sub);
    }

    void multiply(final  int p, final int q) {
        int mul;
        mul = p * q;

        System.out.println("The sum is: " +mul);
    }

    void divide(final double c, final double d) {
        double div;
        div = c / d;

        System.out.println("The sum is: " +div);
    }
}
